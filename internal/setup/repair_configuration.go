package setup

import (
	"fmt"
	"os"
	"path/filepath"
)

// RepairConfiguration handles repairs for configuration-related doctor checks:
//   DSO-DOCTOR-007 (config file missing) — Destructive, requires confirmation
//   DSO-DOCTOR-008 (config empty or invalid YAML) — Destructive, requires confirmation
type RepairConfiguration struct {
	configPath string
	provider   string
	mkdir      func(string, os.FileMode) error
	writeFile  func(string, []byte, os.FileMode) error
}

func newRepairConfiguration(configPath, provider string) *RepairConfiguration {
	return &RepairConfiguration{
		configPath: configPath,
		provider:   provider,
		mkdir:      os.MkdirAll,
		writeFile:  os.WriteFile,
	}
}

func (rc *RepairConfiguration) planForCheck(check DoctorCheck) *RepairAction {
	switch check.ID {
	case "DSO-DOCTOR-007":
		return &RepairAction{
			ID:                   "REPAIR-CFG-001",
			IssueID:              check.ID,
			Category:             DoctorCatConfiguration,
			Description:          fmt.Sprintf("Create default DSO configuration file at %s", rc.configPath),
			RiskLevel:            RepairRiskDestructive,
			RequiresConfirmation: true,
			Status:               RepairStatusPending,
		}
	case "DSO-DOCTOR-008":
		return &RepairAction{
			ID:                   "REPAIR-CFG-002",
			IssueID:              check.ID,
			Category:             DoctorCatConfiguration,
			Description:          fmt.Sprintf("Replace invalid/empty config with defaults at %s", rc.configPath),
			RiskLevel:            RepairRiskDestructive,
			RequiresConfirmation: true,
			Status:               RepairStatusPending,
		}
	}
	return nil
}

// createDefaultConfig creates a minimal DSO configuration file with safe defaults.
func (rc *RepairConfiguration) createDefaultConfig() error {
	return rc.writeConfig()
}

// recreateEmptyConfig replaces an empty or invalid config with defaults.
func (rc *RepairConfiguration) recreateEmptyConfig() error {
	return rc.writeConfig()
}

func (rc *RepairConfiguration) writeConfig() error {
	dir := filepath.Dir(rc.configPath)
	if err := rc.mkdir(dir, 0750); err != nil {
		return fmt.Errorf("create config directory %s: %w", dir, err)
	}
	provider := rc.provider
	if provider == "" {
		provider = "local"
	}
	content := fmt.Sprintf("# DSO configuration — generated by docker dso repair\nprovider: %s\n", provider)
	if err := rc.writeFile(rc.configPath, []byte(content), 0600); err != nil {
		return fmt.Errorf("write config %s: %w", rc.configPath, err)
	}
	return nil
}
